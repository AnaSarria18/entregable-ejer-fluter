void main() {
  // Punto 9: Operadores Aritméticos
  int numero1 = 20;
  int numero2 = 15;

  int suma = numero1 + numero2;
  int resta = numero1 - numero2;
  int multiplicacion = numero1 * numero2;
  double division = numero1 / numero2;

  print('Suma: $suma');
  print('Resta: $resta');
  print('Multiplicación: $multiplicacion');
  print('División: $division');



  // Punto 10: Operadores de Comparación
  bool esIgual = numero1 == numero2;
  bool esMayor = numero1 > numero2;
  bool esDiferente = numero1 != numero2;

  print('¿Son iguales? $esIgual');
  print('¿Es $numero1 mayor que $numero2? $esMayor');
  print('¿Son diferentes? $esDiferente');
}
